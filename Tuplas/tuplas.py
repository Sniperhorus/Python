"""
Una tupla guarda una coleccion de datos inmutable
no se puede modificar!
"""
# Nombre de la variable , signo igual, parentesis
tupla = ('Eduardo', 23, 'mauricio', 23.1)
# Accediendo al valor de una tupla -> tupla[posicion]
print(tupla[-1])

# Marcando limites y saltos

numeros = (1,2,3,4,5,6,7,8,9)
sub = numeros[:5:2]
print(sub)